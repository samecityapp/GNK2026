<h2>1) En Kritik Soru: Kayaköy’e Ne Zaman Gidilir?</h2>
<p>Kayaköy bir yamaç yerleşimi. Evler yukarı doğru katman katman dizilir ve taş zemin güneşi emer. Yazın bu taşlar “ısıtıcı” gibi çalışır: güneşi alır, geri yansıtır. Gölge azdır. Bu yüzden zamanlama her şey.</p>

<div class="bg-red-50 p-4 rounded border-l-4 border-red-400 my-4">
    <h4 class="font-bold text-red-800">Yapılmaması Gereken Hata</h4>
    <p class="text-sm">Temmuz–Ağustos’ta 12:00–16:00 arası Kayaköy’e girmek büyük hata olur. Taşlar ısıyı yükseltir, gölgede bile bunaltır, fotoğraf ışığı sertleşir (kareler “yıkanmış” çıkar). Gezmek değil “kaçmak” istersin.</p>
</div>

<h3>En İyi Saatler (Gerçekten İşe Yarayan İki Pencere)</h3>
<ul class="list-disc pl-6 space-y-2">
    <li><strong>Sabah:</strong> Açılış saatine yakın (genelde 09:00 civarı). Hava daha “taşınabilir”, kalabalık daha az, ışık daha yumuşak.</li>
    <li><strong>Akşamüstü / Gün Batımı Yaklaşırken:</strong> 17:30–19:30 aralığı (mevsime göre kayar). Fotoğrafçıların “altın saat” dediği ışık gelir, taş evlerin rengi turuncuya döner, atmosfer dramatikleşir.</li>
</ul>

<h3>Hangi Ay Daha Keyifli?</h3>
<ul class="list-disc pl-6 space-y-2">
    <li><strong>Nisan–Mayıs:</strong> Serin, rahat yürünür, yeşil daha canlı olur.</li>
    <li><strong>Eylül–Ekim:</strong> Hava hâlâ güzel, kalabalık biraz düşer, ışık harika olur.</li>
    <li><strong>Kış Ayları:</strong> Sessiz ve büyülü olabilir ama yağış/zemin kayganlığına dikkat.</li>
</ul>

