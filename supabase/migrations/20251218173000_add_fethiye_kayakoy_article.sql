<h2>2) Giriş Ücreti, MüzeKart ve “Kapı Seçimi” Taktikleri</h2>
<p>Kayaköy resmi bir ören yeridir. Gişe düzeni ve giriş kapıları dönem dönem farklı uygulamalar gösterebilir; ama mantık aynı: biletini alırsın, rotanı seçersin.</p>

<h3>MüzeKart Konusu (En Doğru Yaklaşım)</h3>
<p>MüzeKart’ın geçerliliği çoğu ören yerinde olduğu gibi genellikle vardır; ama uygulamalar değişebileceği için en güvenlisi şu: Gişede “MüzeKart geçerli mi?” diye teyit et ve güncel ücret/saat bilgisini sor. Bu 30 saniyelik soru, içeride “keşke bilseydim” dedirtmez.</p>

<h3>İki Giriş Kapısı: Hangisi Daha Mantıklı?</h3>
<ul class="list-disc pl-6 space-y-2">
    <li><strong>A) Alt giriş / ana gişe tarafı:</strong> Çoğu ziyaretçi buradan girer. Başlangıç daha düz, sonra tırmanış artar. “Klasik rota” sevenler için rahat.</li>
    <li><strong>B) Üst giriş / yukarıdan başlama:</strong> Daha tepeden başlarsın. Manzara hızlı açılır. Ama ulaşım/ara sokaklar biraz daha karışık gelebilir.</li>
</ul>

