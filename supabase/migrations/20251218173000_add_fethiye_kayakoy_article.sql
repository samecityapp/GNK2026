<h2>5) Kayaköy’de Sıcaktan Korunma ve Konfor Listesi</h2>
<p>Kayaköy’de “rahat gezmek” için küçük hazırlık büyük fark yaratır:</p>
<ul class="list-disc pl-6 space-y-2">
    <li>✅ <strong>Ayakkabı:</strong> Kaymayan taban + rahat yürüyüş (terlik gerçekten eziyet).</li>
    <li>✅ <strong>Su:</strong> En az 1 litre (sıcakta 1,5 litre).</li>
    <li>✅ <strong>Şapka + güneş gözlüğü:</strong> Gölgede yürümeyeceğini kabul et.</li>
    <li>✅ <strong>Güneş kremi:</strong> Rüzgâr yanmayı gizler.</li>
    <li>✅ <strong>Mini atıştırmalık:</strong> Kan şekeri düşerse keyif biter.</li>
    <li>✅ <strong>Mola planı:</strong> 20–25 dakikada bir 2 dakika durmak, toplam enerjiyi korur.</li>
</ul>

