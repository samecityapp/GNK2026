<h1>Tarih ve Doğa İç İçe</h1>
<h2>Hayalet Köy Kayaköy’ü Gezmek İçin En Doğru Saatler ve Rotalar</h2>

<p>Fethiye tatilinde denizden kafanı kaldırıp tek bir kültür durağı seçeceksen, bence Kayaköy listenin başında olmalı. Çünkü burası “birkaç taş ev” değil; sessizliğiyle konuşan, yürüdükçe içine çekilen, fotoğrafların bile “hüzünlü” çıktığı bir açık hava müzesi.</p>

<p>Eski adıyla Levissi olarak bilinen Kayaköy, 1923’teki mübadele öncesinde canlı bir yerleşimdi: kiliseler, okullar, sokaklar, evler… Bugünse çatısız taş evleri ve yamaçlara yayılan planıyla dünya çapında “hayalet köy” olarak anılan bir ören yeri.</p>

<p>Ama Kayaköy’ün bir gerçeği daha var: Fethiye’nin en sıcak, en taşlık ve en gölgesiz gezilerinden biri olabilir. Yanlış saatte gidersen “kültür turu” değil “sıcakta hayatta kalma”ya döner. Doğru saatte gidersen de inanılmaz keyifli olur.</p>

<p>Bu rehberde sana şunları netçe vereceğim:</p>
<ul class="list-disc pl-6 space-y-2">
    <li>Kayaköy’e hangi saatlerde gidilir (sıcaktan kaçış planı)</li>
    <li>En verimli yürüyüş rotaları (kısa tur / tam tur)</li>
    <li>Fotoğraf için en iyi ışık ve noktalar</li>
    <li>Bilet/MüzeKart işini hatasız çözme</li>
    <li>Geziden sonra dinlenme ve “ödül” planı (mekân adı vermeden)</li>
</ul>

<p class="mt-4 text-sm text-gray-500">Not: Senin kuralın gereği işletme/otel/kişi adı kullanmıyorum. Sadece bölge, deneyim ve pratik taktik.</p>

