<h3>Rota A: “1 Saatlik” Kayaköy (Az ama Öz)</h3>
<p><strong>Kimler için?</strong> Kültür turu yapmak istiyor ama günü plaja da ayıracak olanlar.</p>
<ul class="list-disc pl-6 space-y-2">
    <li><strong>Alt bölümde kısa keşif:</strong> İlk ev kümelerini dolaş, ritmi yakala.</li>
    <li><strong>Ana patikayı bul:</strong> Geniş yürüyüş hattını takip ederek yukarı doğru ilerle.</li>
    <li><strong>Manzara noktası:</strong> Çok tırmanmadan, vadinin açıldığı bir noktada 5–10 dakika mola.</li>
    <li><strong>Geri dönüş:</strong> Aynı hattı geri dönerek çık.</li>
</ul>
<p><strong>Neden işe yarıyor?</strong> Kayaköy’ün ruhunu alırsın ama “taş fırında pişmiş insan” gibi çıkmazsın.</p>

<h3>Rota B: “Tam Kayaköy” (2–3 Saatlik, İyi Tempo İle)</h3>
<p><strong>Kimler için?</strong> “Ben bir kere geldim, hakkını vereyim” diyenler.</p>
<ul class="list-disc pl-6 space-y-2">
    <li><strong>Alt Kilise bölgesi:</strong> Girişe yakın, ilk güçlü durak. Fresk/duvar kalıntılarını sakin sakin gez.</li>
    <li><strong>Ana cadde hattı:</strong> Evlerin arasından yukarı doğru yükselen geniş patikaya gir.</li>
    <li><strong>Üst Kilise:</strong> Tepede büyük yapı ve mozaik detaylarıyla en etkileyici noktalardan. Burada manzara molası şart.</li>
    <li><strong>Şapel / manzara tepesi:</strong> Kondisyonun varsa üst kilisenin de üzerine çıkan kısım var. Burada hem Kayaköy vadisi hem de uzakta deniz perspektifi açılır.</li>
</ul>
<p><strong>Bu rotanın sırrı:</strong> Tüm keyif “tümünü görmek” değil, doğru yerlerde durmak. Her sokağa girmeye çalışırsan 3 saat değil 30 dakika sonra “tamam” dersin.</p>

